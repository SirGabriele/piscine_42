int	ft_str_is_numeric(char *str)
{
	int	index;

	index = 0;
	while (str[index] != '\0')
	{
		if (str[index] <= 57 && str[index] >= 48)
		{
			index++;
		}
		else
			return (0);
	}
	return (1);
}
